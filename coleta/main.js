// Funções Matemáticas
let num = 4.7

// Arredor números
console.log(Math.round(num)) //inteiro mais próximo
console.log(Math.floor(num)) //arredonda para baixo
console.log(Math.ceil(num)) //arredonda para cima

// Número Aleatório
console.log(Math.random()) //número aleatório entre 0 a 1
console.log(Math.random()*10) //número aleatório entre 0 a 10